class Noticia {
    constructor(
        Id,
        Fecha,
        Imagen,
        Autor,
        Tipo,
        Titulo,
        Url
    ){
        this.Id=Id;
        this.Fecha=Fecha;
        this.Imagen=Imagen;
        this.Autor=Autor;
        this.Tipo=Tipo;
        this.Titulo=Titulo;
        this.Url=Url;
    }
}

export default Noticia;