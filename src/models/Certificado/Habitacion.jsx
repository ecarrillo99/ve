class Habitacion{
    constructor(
        Nombre,
        Cantidad,
        Subtotal,
        Impuestos,
        Total,
        Incluye,
        Acomodacion
    ){
        this.Nombre=Nombre;
        this.Cantidad=Cantidad;
        this.Subtotal=Subtotal;
        this.Impuestos=Impuestos;
        this.Total=Total;
        this.Incluye=Incluye;
        this.Acomodacion=Acomodacion;
    }
}
export default Habitacion;