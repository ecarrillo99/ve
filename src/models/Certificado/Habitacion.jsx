class Habitacion{
    constructor(
        Nombre,
        Cantidad,
        Subtotal,
        Impuestos,
        Total,
        Acomodacion,
        Ninos,
        Adultos,
    ){
        this.Nombre=Nombre;
        this.Cantidad=Cantidad;
        this.Subtotal=Subtotal;
        this.Impuestos=Impuestos;
        this.Total=Total;
        this.Acomodacion=Acomodacion;
        this.Ninos=Ninos;
        this.Adultos=Adultos;
    }
}
export default Habitacion;